
public class Person {
	private String name;
	private String adress;
	private long phoneNumber;
	private String emailAddress;
	
	public Person(String name, String adress, long phoneNumber, String emailAddress) {
		super();
		this.name = name;
		this.adress = adress;
		this.phoneNumber = phoneNumber;
		this.emailAddress = emailAddress;
	}
	
	
}
